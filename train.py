from ultralytics import YOLO

# 1. โหลดโมเดลตั้งต้น
# เราจะใช้ 'yolov8n.pt' ซึ่งเป็นโมเดลที่เล็กและเร็วที่สุด เหมาะสำหรับเริ่มต้น
# โมเดลนี้มีความรู้พื้นฐานเรื่องการมองเห็นวัตถุอยู่แล้ว
model = YOLO('yolov8n.pt') 

# 2. เริ่มการเทรน (Fine-tuning)
# บอกให้โมเดลเรียนรู้เพิ่มเติมจากข้อมูลของเรา
results = model.train(
    data='solar_dataset.yaml',  # ไฟล์ config ที่เราสร้างไว้
    epochs=50,                  # จำนวนรอบในการเรียนรู้ (เริ่มต้นที่ 50 กำลังดี)
    imgsz=640,                  # ขนาดของภาพที่จะใช้เทรน
    batch=8                     # จำนวนภาพที่ป้อนให้ AI เรียนรู้ในแต่ละครั้ง
)

print("การเทรนเสร็จสิ้น! โมเดลที่ดีที่สุดถูกบันทึกไว้ที่โฟลเดอร์ runs/detect/train/weights/best.pt")